package org.example;


/**
 * The Listable interface is a marker interface for objects that can be included in an enumeration of items converted to strings.
 *
 * <p>
 * Implementing this interface indicates that an object should have a meaningful {@code toString()} method to represent its contents as a string.
 * </p>
 *
 * <p>
 * Note: Implementing classes should override the {@code toString()} method to provide a customized string representation of the object's state.
 * </p>
 */
public interface Listable {}
